    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String format = "[1-9]+";//[1-9]+
        if (txtSoLuong.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Phai dien so luong");
        } else if (!txtSoLuong.getText().trim().matches(format)) {
            JOptionPane.showMessageDialog(this, "So luong phai la so nguyen duong");
        } else {
            // Lay du lieu tu form
            DonHang donhang = layDuLieuTuForm();
            // them doi tuong vao danh sach
            listDonHang.add(donhang);
            // Do danh sach moi len bang
            doDuLieuLenBang();
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        // Lay vi tri doi truong da chon tren bang
        String format = "[1-9]+";//[1-9]+
        if (txtSoLuong.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Phai dien so luong");
        } else if (!txtSoLuong.getText().trim().matches(format)) {
            JOptionPane.showMessageDialog(this, "So luong phai la so nguyen duong");
        } else {
            int rowIndex = tblDanhSach.getSelectedRow();
            // Lay duoc du lieu doi tuong duoc sua tren form
            DonHang dh = layDuLieuTuForm();
            listDonHang.set(rowIndex, dh);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int rowIndex = tblDanhSach.getSelectedRow();
        if (rowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Chon doi tuong can xoa");
        } else {
            // 0: Yes; 1: No; 2: Cancel
            int confirm = JOptionPane.showConfirmDialog(this, "Co chac chan muon xoa khong");
            if (confirm == 0) {
                listDonHang.remove(rowIndex);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

