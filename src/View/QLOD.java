    private void doDuLieuLenBang() {
        // Do du lieu tu dong dau tien
        defaultTable.setRowCount(0);
        // Duyet tung phan tu do du lieu len bang
        for (DonHang dh : listDonHang) {
            defaultTable.addRow(new Object[]{dh.getMon(), dh.getSoLuong(),
                dh.getTopping(), dh.getGhiChu(), dh.isThanhToan()});
        }
    }

    private void doDuLieuLenForm(DonHang dh) {
        // Do du lieu len combobox
        cboMon.setSelectedItem(dh.getMon());
        // Do du lieu len textField So luong 
        txtSoLuong.setText(String.valueOf(dh.getSoLuong()));
        if (dh.getTopping().equals("Trân châu den")) {
            rdoTTD.setSelected(true);
        } else if (dh.getTopping().equals("Trân châu trang")) {
            rdoTTT.setSelected(true);
        } else if (dh.getTopping().equals("Thach")) {
            rdoT.setSelected(true);
        } else {
            rdoTTD.setSelected(false);
            rdoTTT.setSelected(false);
            rdoT.setSelected(false);
        }
    }

    // Lay du lieu tu form
    private DonHang layDuLieuTuForm() {
        String topping = "";
        if (rdoTTD.isSelected()) {
            topping = "Trân châu den";
        } else if (rdoTTT.isSelected()) {
            topping = "Trân châu trang";
        } else if (rdoT.isSelected()) {
            topping = "Thach";
        }
//        DonHang dh = new DonHang(cboMon.getSelectedItem().toString(),
//                Integer.parseInt(txtSoLuong.getText()),
//                rdoTTD.isSelected() ? "Trân châu den" : rdoTTT.isSelected() ? "Trân châu trang"
//                : rdoT.isSelected() ? "Thach" : "", ghiChu, rootPaneCheckingEnabled);
        int soluong = 0;
        try {
            soluong = Integer.parseInt(txtSoLuong.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "So luong phai la so nguyen duong");
        }
        DonHang dh = new DonHang(cboMon.getSelectedItem().toString(),
                soluong, topping, txtGhiChu.getText(), chkThanhToan.isSelected());
        return dh;
    }

